package com.mycompany.paipajava.vo;

public class ObjectVO {

    /*Todo los atributos*/
    String sn;
    String frame;
    String puerto;

    public ObjectVO() {
    }

    public String getSn() {
        return sn;
    }

    public void setSn(String sn) {
        this.sn = sn;
    }

    public String getFrame() {
        return frame;
    }

    public void setFrame(String frame) {
        this.frame = frame;
    }

    public String getPuerto() {
        return puerto;
    }

    public void setPuerto(String puerto) {
        this.puerto = puerto;
    }

}
